-- Câu 17
SELECT n.TENNV, p.TENPHONG
FROM NHANVIEN n
JOIN PHONGBAN p ON n.MAPHONG = p.MAPHONG
JOIN DIADIEM d ON p.MADIADIEM = d.MADIADIEM
WHERE d.THANHPHO = 'TORONTO';

