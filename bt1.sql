-- Bảng DIADIEM_PHG
CREATE TABLE DIADIEM_PHG (
    MAPHG NUMBER,
    DIADIEM NVARCHAR2(50),
    PRIMARY KEY (MAPHG, DIADIEM)
);

-- Bảng PHONGBAN
CREATE TABLE PHONGBAN (
    MAPHG NUMBER PRIMARY KEY,
    TENPHG NVARCHAR2(50),
    TRPHG NUMBER,
    NG_NHANCHUC DATE
);

-- Bảng NHANVIEN
CREATE TABLE NHANVIEN (
    MANV NUMBER PRIMARY KEY,
    HONV NVARCHAR2(20),
    TENLOT NVARCHAR2(20),
    TENNV NVARCHAR2(20),
    NGSINH DATE,
    DCHI NVARCHAR2(100),
    PHAI NVARCHAR2(5),
    LUONG NUMBER,
    MA_NQL NUMBER,
    PHG NUMBER,
    CONSTRAINT FK_NV_PHG FOREIGN KEY (PHG) REFERENCES PHONGBAN(MAPHG)
);

-- Bảng DEAN
CREATE TABLE DEAN (
    MADA NUMBER PRIMARY KEY,
    TENDA NVARCHAR2(50),
    DDIEM_DA NVARCHAR2(50),
    PHONG NUMBER,
    CONSTRAINT FK_DA_PHG FOREIGN KEY (PHONG) REFERENCES PHONGBAN(MAPHG)
);

-- Bảng THANNHAN
CREATE TABLE THANNHAN (
    MA_NVIEN NUMBER,
    TENTN NVARCHAR2(50),
    PHAI NVARCHAR2(5),
    NGSINH DATE,
    QUANHE NVARCHAR2(20),
    PRIMARY KEY (MA_NVIEN, TENTN),
    CONSTRAINT FK_TN_NV FOREIGN KEY (MA_NVIEN) REFERENCES NHANVIEN(MANV)
);

-- Bảng PHANCONG
CREATE TABLE PHANCONG (
    MA_NVIEN NUMBER,
    MADA NUMBER,
    THOIGIAN NUMBER,
    PRIMARY KEY (MA_NVIEN, MADA),
    CONSTRAINT FK_PC_NV FOREIGN KEY (MA_NVIEN) REFERENCES NHANVIEN(MANV),
    CONSTRAINT FK_PC_DA FOREIGN KEY (MADA) REFERENCES DEAN(MADA)
);

